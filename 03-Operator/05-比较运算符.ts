// 大于
console.log(1 > 2); // false

// 大于等于
console.log(1 >= 2); // false

// 小于
console.log(1 < 2); // true

// 小于等于
console.log(1 <= 2); // true

// 相等
let num1: number = 1;
let num2: number = 2;
console.log(num1 === num2); // false

// 不相等
let num3: number = 1;
let num4: number = 2;
console.log(num3 !== num4); // true